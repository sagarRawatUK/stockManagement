    public void load(){
        int a;
        try {
            pst = con.prepareStatement("select * from suppliers");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df =(DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i =1;i<=a;i++){
                    v2.add(rs.getString("supplier_id"));
                    v2.add(rs.getString("supplier_name"));
                    v2.add(rs.getString("email"));
                    v2.add(rs.getString("GSTN"));
                    v2.add(rs.getString("phone_num"));
                    v2.add(rs.getString("address"));
                }
                df.addRow(v2);
            }
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(suppliers.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

