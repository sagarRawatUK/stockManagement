    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        df = (DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        int id = Integer.parseInt(df.getValueAt(selected,0).toString());
        
        String name = txtsuppliername.getText();
            String email = txtemail.getText();
            String gstn = txtgstn.getText();
            String phone = txtphone.getText();
            String address = txtaddress.getText();
            
            
        try {
            pst = con.prepareStatement("update suppliers set supplier_name=?,email=?,GSTN=?,phone_num=?,address=? where supplier_id=?");
            pst.setString(1,name);
            pst.setString(2,email);
            pst.setString(3,gstn);
            pst.setString(4,phone);
            pst.setString(5,address);
            pst.setInt(6,id);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Supplier Updated");
            txtsuppliername.setText("");
            txtemail.setText("");
            txtgstn.setText("");
            txtphone.setText("");
            txtaddress.setText("");
            txtsuppliername.requestFocus();
            load();
            jButton1.setEnabled(true);
        
            
        } catch (SQLException ex) {
            Logger.getLogger(suppliers.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
        
    }//GEN-LAST:event_jButton3ActionPerformed

