    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            
            String name = txtsuppliername.getText();
            String email = txtemail.getText();
            String gstn = txtgstn.getText();
            String phone = txtphone.getText();
            String address = txtaddress.getText();
            
            
            pst = con.prepareStatement("insert into suppliers(supplier_name,email,GSTN,phone_num,address) values(?,?,?,?,?)");
            
            pst.setString(1,name);
            pst.setString(2,email);
            pst.setString(3,gstn);
            pst.setString(4,phone);
            pst.setString(5,address);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Supplier Added");
            txtsuppliername.setText("");
            txtemail.setText("");
            txtgstn.setText("");
            txtphone.setText("");
            txtaddress.setText("");
            txtsuppliername.requestFocus();
            load();
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(suppliers.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

